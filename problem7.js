// Write a program to take a day as an input and determine whether it is a weekday or weekend. Example: Tuesday is weekday.

let day = "sunday"

switch (day) {
    case "monday":
        console.log("monday is a weekday")
        break;
    case "tuesday":
        console.log("tuesday is a weekday")
        break;
    case "wednesday":
        console.log("wednesday is a weekday")
        break;
    case "thursday":
        console.log("thursday is a weekday")
        break;
    case "friday":
        console.log("friday is a weekday")
        break;
    case "saturday":
        console.log("saturday is a weekend")
        break;
    case "sunday":
        console.log("sunday is a weekend")
        break;

    default:
        "maa chuda"
        break;
}

